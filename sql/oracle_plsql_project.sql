CREATE OR REPLACE TRIGGER trg_before_appointment_validate
BEFORE INSERT OR UPDATE ON appointments
FOR EACH ROW
BEGIN
  IF :NEW.appointment_type = 'EMERGENCY' THEN
    :NEW.priority := 0;
  ELSIF :NEW.appointment_type = 'SCHEDULED' THEN
    :NEW.priority := 1;
  ELSIF :NEW.appointment_type = 'WALK_IN' THEN
    :NEW.priority := 2;
  END IF;
END;
/

CREATE OR REPLACE TRIGGER trg_before_status_validate
BEFORE UPDATE OF status ON appointments
FOR EACH ROW
BEGIN
  IF :OLD.status = 'COMPLETED' AND :NEW.status <> 'COMPLETED' THEN
    RAISE_APPLICATION_ERROR(-20010, 'Completed appointments cannot move back to another status.');
  END IF;

  IF :OLD.status = 'CANCELLED' AND :NEW.status NOT IN ('CANCELLED', 'BOOKED') THEN
    RAISE_APPLICATION_ERROR(-20011, 'Cancelled appointments can only remain cancelled or be rescheduled.');
  END IF;
END;
/

CREATE OR REPLACE TRIGGER trg_after_consultation_duration
BEFORE INSERT OR UPDATE OF end_time ON consultations
FOR EACH ROW
WHEN (NEW.end_time IS NOT NULL)
BEGIN
  :NEW.duration := GREATEST(1, ROUND((:NEW.end_time - :NEW.start_time) * 24 * 60));
END;
/

CREATE OR REPLACE TRIGGER trg_after_consultation_notify
AFTER UPDATE OF end_time ON consultations
FOR EACH ROW
WHEN (NEW.end_time IS NOT NULL)
BEGIN
  INSERT INTO notifications(appointment_id, message)
  VALUES (:NEW.appointment_id, 'Consultation completed. Thank you for visiting.');
END;
/

CREATE OR REPLACE TRIGGER trg_after_cancel_release
AFTER UPDATE OF status ON appointments
FOR EACH ROW
WHEN (NEW.status = 'CANCELLED')
BEGIN
  DELETE FROM queue_status WHERE appointment_id = :NEW.appointment_id;

  INSERT INTO notifications(appointment_id, message)
  VALUES (:NEW.appointment_id, 'Appointment cancelled and slot released for reuse.');
END;
/

CREATE OR REPLACE TRIGGER trg_after_delay_notify
AFTER INSERT ON delay_log
FOR EACH ROW
BEGIN
  INSERT INTO notifications(appointment_id, message)
  SELECT appointment_id, :NEW.message
  FROM appointments
  WHERE doctor_id = :NEW.doctor_id
    AND status IN ('BOOKED', 'CHECKED_IN')
    AND appointment_id = NVL(:NEW.appointment_id, appointment_id);
END;
